public abstract class AbstractItem {

    public abstract String getItem_name();

    public abstract void setItem_name(String item_name);

    public abstract String getCode();

    public abstract void setCode(String code);

    public abstract String getQuantity() ;

    public abstract void setQuantity(String quantity);

    public abstract String getExpirationDate();

    public abstract void setExpirationDate(String expiration_date);
}
